uint32_t GetSignalVrms(void)
{
    uint32_t    res;
    float       Vrms;

    _GetVrmsF = 0;
    _TotVrms[0] = 0;
    _TotVrms[1] = 0;
    _TotVrmsCnt = 0;
    _GetVrmsF = 1;
    while (_GetVrmsF) {}

    Vrms = AppInfo.AdcRatio[1] / SIGNAL_RESULT_MAGIC * sqrt((float)_TotVrms[1] / CALC_SIGNAL_SAMPLE_COUNT);
    res = Vrms;
    res <<= 16;
    Vrms = AppInfo.AdcRatio[0] / SIGNAL_RESULT_MAGIC * sqrt((float)_TotVrms[0] / CALC_SIGNAL_SAMPLE_COUNT);
    res += Vrms;

    return res;
}

uint32_t GetSignalOffset(void)
{
    uint32_t res;

    _GetDcOffsetF = 0;
    _TotOffset[0] = 0;
    _TotOffset[1] = 0;
    _TotOffsetCnt = 0;
    _GetDcOffsetF = 1;
    while (_GetDcOffsetF) {}

    res = _TotOffset[0] >> CALC_SIGNAL_SAMPLE_BITS;
    res += ((_TotOffset[1] >> CALC_SIGNAL_SAMPLE_BITS) << 16);

    return res;
}

