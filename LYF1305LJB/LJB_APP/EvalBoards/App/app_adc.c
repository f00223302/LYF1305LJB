void AppTaskADC(void *p_arg)
{
    (void)p_arg;

    while (DEF_TRUE)
    {
        AppInfo.TaskAdcCnt++;

        if (AppInfo.RdyFlag)
        {
            BSP_Check_RestADC1(BSP_Info.ADCBuf[0]);

            AdcDataHandler();
        }

        OSTimeDly(10);
    }
}


/// Tim3 中断中调用：
uint32_t    Adc_Tm3CallBack(unsigned char Arg, void *ptr)
{
    if (AppInfo.RdyFlag == 1)
    {

        SaveAdcData();

        ///重新启动 ADC 采样：
        BSP_ADC_Start();

        ///TIM3 监控变量
        AppInfo.IntTm3Cnt++;
    }

    return 0;
}

