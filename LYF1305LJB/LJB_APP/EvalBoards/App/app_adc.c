void GetSampleOffset(uint16_t *buf)
{
    uint16_t i, j;
    uint32_t tot[POWER_SAMPLE_CHANNEL_COUNT];

    for (i = 0; i < POWER_SAMPLE_CHANNEL_COUNT; i++)
    {
        tot[i] = 0;
        for (j = 0; j < _RES_FRAME_SIZE; j++)
        {
            tot[i] += _ResBuf[j][i];
        }

        buf[i] = tot[i] / _RES_FRAME_SIZE;
    }

}


