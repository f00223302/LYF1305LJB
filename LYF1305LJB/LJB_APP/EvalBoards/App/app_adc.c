void GetTemperature(uint8_t Index)
{
    uint8_t j, m, n;
    uint16_t data;

    _TotTemper += _ResBuf[Index][26];
    _TemperCnt++;
    if (_TemperCnt >= 2048)
    {

        data = _TotTemper >> 11;
        _TotTemper = 0;
        _TemperCnt = 0;
        if (data <= _TemperTbl[0])
            _Temper = 20 + 0x80;
        else if (data > _TemperTbl[120])
            _Temper = 100;
        else
        {

            m = 1;
            n = 120;
            while (1)
            {

                if (n == m + 1)
                {

                    j = n;
                    break;
                }
                else
                {

                    j = m + ((n - m) >> 1);
                    if (data == _TemperTbl[j])
                        break;
                    else if (data < _TemperTbl[j])
                        n = j;
                    else
                        m = j;
                }
            }

            if (j < 20)
                _Temper = 0x80 + (20 - j);
            else
                _Temper = j - 20;
        }
    }
}
